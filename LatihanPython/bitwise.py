a = 2
b = 9

#OR (|)
print('\n========OR========')
c = a | b
print('nilai = ',a,'binary = ', format(a,'08b'))
print('nilai = ',b,'binary = ', format(b,'08b'))
print('____________________(|)')
print('nilai = ',c,'binary = ', format(c,'08b'))

#AND (&)
print('\n========AND========')
c = a & b
print('nilai = ',a,'binary = ', format(a,'08b'))
print('nilai = ',b,'binary = ', format(b,'08b'))
print('____________________(&)')
print('nilai = ',c,'binary = ', format(c,'08b'))

#XOR (^)
print('\n========XOR========')
c = a ^ b
print('nilai = ',a,'binary = ', format(a,'08b'))
print('nilai = ',b,'binary = ', format(b,'08b'))
print('____________________(^)')
print('nilai = ',c,'binary = ', format(c,'08b'))

#NOT (~)
print('\n========NOT========')
c = ~a
print('nilai = ',c,'binary = ', format(c,'08b'))

